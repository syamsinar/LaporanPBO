package Laporan3;

public class Unary {
	public static void main (String [] args){
			int hasil = +1;
			System.out.println (hasil);
			
			hasil--;
			System.out.println (hasil);
			
			hasil++;
			System.out.println (hasil);
			
			hasil = -hasil;
			System.out.println (hasil);
			
			boolean sukses = false;
			System.out.println (sukses);
			System.out.println (!sukses);
		}
}
