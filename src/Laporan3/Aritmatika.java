package Laporan3;

public class Aritmatika {
	public static void main (String[] args) {
		int hasil = 1 + 2;
		System.out.println(hasil);
		
		hasil = hasil - 1;
		System.out.println(hasil);
		
		hasil = hasil * 2;
		System.out.println(hasil);
		
		
		hasil = hasil / 2;
		System.out.println(hasil);
		
		hasil = hasil + 8;
		hasil = hasil % 7;
		System.out.println(hasil);
		
		hasil = hasil+= 1;
		System.out.println(hasil);
		
		hasil = hasil-= 2;
		System.out.println(hasil);
		
		hasil = hasil*= 4;
		System.out.println(hasil);

	}
}


