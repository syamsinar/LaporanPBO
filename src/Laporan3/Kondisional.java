public class Kondisional {
		public static void main(String[] args) {
			int nilai1 =1;
			int nilai2 =2;
			if ((nilai1 == 1) && (nilai2 == 2))
				System.out.println ("nilai1 adalah 1 AND nilai2 adalah 2");
			if ((nilai1 == 1) || (nilai2 == 1))				
				System.out.println ("nilai1 adalah 1 OR nilai2 adalah 1");
			}

}
